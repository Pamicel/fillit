int       ft_column_max(unsigned short *tetro, int x)
{
    unsigned short modula;
    int i;

    i = 0;
    modula = 0b1000000000000000;
    modula >> (x - 1);

    while (tetro[i] % modula == 0)
	{
		if (i == 3)
			return (1);
		i++;
	}
    return (0);
}


